package week2.day4;

public class PrintEvenNumber {
public static void main(String[] args) {
	//pseudocode
	//Iterate the values from 1 to 20
	//check if the number is even
	//if it is match print even number
	
	for (int i = 1; i <=20; i++) {
		
		if(i%2==0) {
			System.out.println(i);
		}
	}
	
}
}
